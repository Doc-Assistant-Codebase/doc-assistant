package com.doc.assistant.model;

public class Medicine {
	private Integer id;
	private String name;
	
	public Medicine(){ }

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}